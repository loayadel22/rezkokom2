export const getProductById = (id: number): Product | undefined => {
  return products.find((product) => product.id === id);
};

export const getFlashSaleProducts = (): Product[] => {
  return products.filter((product) => product.onSale).slice(0, 8);
};

export const getBestSellingProducts = (): Product[] => {
  return products.filter((product) => product.rating >= 4.7).slice(0, 8);
};

export const getAllProducts = (): Product[] => {
  return products;
};

export const getProductsByCategory = (category: string): Product[] => {
  return products.filter((product) => product.category === category);
};

export const getNewProducts = (): Product[] => {
  return products.filter((product) => product.isNew).slice(0, 8);
};

export const getFeaturedProducts = (): Product[] => {
  return products
    .filter((product) => product.rating >= 4.8 && product.totalReviews >= 200)
    .slice(0, 6);
};
